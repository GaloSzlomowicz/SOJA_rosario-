# ======= EXPORTAR RESULTADOS A EXCEL ==============
# --------------------------------------------------
def exportar_resultados(data, resultados_coint, metricas, modelo, cfg=CONFIG):
    writer = pd.ExcelWriter(cfg.get('output_excel', 'resultado_reversion.xlsx'), engine='xlsxwriter')
    data.to_excel(writer, sheet_name='data', index=False)
    pd.DataFrame([resultados_coint]).to_excel(writer, sheet_name='cointegracion', index=False)
    pd.DataFrame([metricas]).to_excel(writer, sheet_name='metricas', index=False)
    if modelo is not None:
        # parametros del modelo
        params = modelo.params
        try:
            summary_df = pd.read_html(modelo.summary().tables[1].as_html(), header=0, index_col=0)[0]
            summary_df.to_excel(writer, sheet_name='regression_summary')
        except Exception:
            pd.DataFrame(params).to_excel(writer, sheet_name='regression_params')
    writer.save()
    print(f"Resultados exportados a: {cfg.get('output_excel')}")

# --------------------------------------------------
# ======= MAIN =====================================
# --------------------------------------------------
def run_all(cfg=CONFIG):
    data = cargar_series(cfg)
    modelo, data = regresion_simple(data)
    resultados_coint = analizar_cointegracion(data, cfg)
    data = calcular_zscores_y_senales(data, cfg)
    metricas = calcular_metricas_trading(data, cfg)
    panel_visual(data, resultados_coint, metricas, cfg)
    exportar_resultados(data, resultados_coint, metricas, modelo, cfg)

    # resumen impreso
    print('\n=== Resumen rapido ===')
    print(f"Cointegracion (p-value): {resultados_coint.get('coint_pvalue', np.nan)}")
    print(f"ADF spread (p-value): {resultados_coint.get('adf_pvalue', np.nan)}")
    print(f"ADF resid (p-value): {resultados_coint.get('adf_resid_pvalue', np.nan)}")
    if modelo is not None:
        print(f"Regresion R2: {modelo.rsquared:.4f} | beta: {modelo.params.get('NOV', np.nan):.4f}")

    return data, modelo, resultados_coint, metricas

# --------------------------------------------------
if __name__ == '__main__':
    run_all(CONFIG)