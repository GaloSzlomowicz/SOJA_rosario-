# ======= Z-SCORES Y SEÑALES (ESTÁTICO + ROLLING) =
# --------------------------------------------------
def calcular_zscores_y_senales(data, cfg=CONFIG):
    df = data.copy()
    # Z-score estático del spread
    df["Zscore_Spread"] = (df["Spread"] - df["Spread"].mean()) / df["Spread"].std()

    # Z-score del residuo global
    if "Residuo" in df.columns and not df["Residuo"].dropna().empty:
        df["Zscore_Residuo"] = (df["Residuo"] - df["Residuo"].mean()) / df["Residuo"].std()
    else:
        df["Zscore_Residuo"] = np.nan

    # Z-score rolling (adaptativo)
    w = cfg.get("rolling_window", 60)
    if len(df) >= w:
        df["Residuo_roll_mean"] = df["Residuo"].rolling(window=w, min_periods=int(w/2)).mean()
        df["Residuo_roll_std"] = df["Residuo"].rolling(window=w, min_periods=int(w/2)).std()
        df["Zscore_Residuo_roll"] = (df["Residuo"] - df["Residuo_roll_mean"]) / df["Residuo_roll_std"]
    else:
        df["Residuo_roll_mean"] = np.nan
        df["Residuo_roll_std"] = np.nan
        df["Zscore_Residuo_roll"] = np.nan

    # Señales: priorizamos Zscore_Residuo_roll si existe, sino Zscore_Residuo, sino Zscore_Spread
    umbral = cfg.get("zscore_umbral", 1.5)
    df["Se\u00f1al"] = 0

    zcol = "Zscore_Residuo_roll" if "Zscore_Residuo_roll" in df.columns and not df["Zscore_Residuo_roll"].dropna().empty else (
        "Zscore_Residuo" if not df["Zscore_Residuo"].dropna().empty else "Zscore_Spread"
    )

    if zcol in df.columns:
        df.loc[df[zcol] < -umbral, "Se\u00f1al"] = 1   # Compra: spread bajo / residuo negativo
        df.loc[df[zcol] > umbral, "Se\u00f1al"] = -1  # Venta: spread alto / residuo positivo

    return df

