# ======= TESTS DE COINTEGRACION Y ADF =============
# --------------------------------------------------
def analizar_cointegracion(data, cfg=CONFIG):
    """Realiza coint(NOV,DIC) y ADF sobre Spread y sobre Residuo (si existe)."""
    resultados = {}
    df = data.dropna(subset=["NOV", "DIC"]) if "NOV" in data.columns and "DIC" in data.columns else data

    # Engle-Granger
    if len(df) < 10:
        resultados["coint_score"] = np.nan
        resultados["coint_pvalue"] = np.nan
        resultados["coint_rechaza"] = False
    else:
        try:
            score, pvalue, _ = coint(df["NOV"], df["DIC"])  # NOV first, DIC second
            resultados["coint_score"] = float(score)
            resultados["coint_pvalue"] = float(pvalue)
            resultados["coint_rechaza"] = pvalue < cfg["coint_pvalue_max"]
        except Exception as e:
            resultados["coint_score"] = np.nan
            resultados["coint_pvalue"] = np.nan
            resultados["coint_rechaza"] = False

    # ADF sobre Spread
    spread = data["Spread"].dropna()
    if len(spread) < 10:
        resultados["adf_statistic"] = np.nan
        resultados["adf_pvalue"] = np.nan
        resultados["adf_rechaza"] = False
    else:
        try:
            adf_res = adfuller(spread, autolag='AIC')
            resultados["adf_statistic"] = float(adf_res[0])
            resultados["adf_pvalue"] = float(adf_res[1])
            resultados["adf_rechaza"] = adf_res[1] < cfg["coint_pvalue_max"]
        except Exception:
            resultados["adf_statistic"] = np.nan
            resultados["adf_pvalue"] = np.nan
            resultados["adf_rechaza"] = False

    # ADF sobre Residuo (si disponible)
    if "Residuo" in data.columns and not data["Residuo"].dropna().empty:
        resid = data["Residuo"].dropna()
        if len(resid) >= 10:
            try:
                adf_r = adfuller(resid, autolag='AIC')
                resultados["adf_resid_statistic"] = float(adf_r[0])
                resultados["adf_resid_pvalue"] = float(adf_r[1])
                resultados["adf_resid_rechaza"] = adf_r[1] < cfg["coint_pvalue_max"]
            except Exception:
                resultados["adf_resid_statistic"] = np.nan
                resultados["adf_resid_pvalue"] = np.nan
                resultados["adf_resid_rechaza"] = False
        else:
            resultados["adf_resid_statistic"] = np.nan
            resultados["adf_resid_pvalue"] = np.nan
            resultados["adf_resid_rechaza"] = False

    return resultados

