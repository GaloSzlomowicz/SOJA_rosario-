# ======= REGRESIÓN SIMPLE (DIC ~ NOV) =============
# --------------------------------------------------
def regresion_simple(data):
    """Ajusta DIC ~ const + beta * NOV. Devuelve modelo y DataFrame con Predicha y Residuo."""
    df = data.copy()
    df_clean = df.dropna(subset=["NOV", "DIC"]).copy()
    if len(df_clean) < 5:
        print("Datos insuficientes para regresi\u00f3n (menos de 5 observaciones).")
        df["Predicha"] = np.nan
        df["Residuo"] = np.nan
        return None, df

    X = sm.add_constant(df_clean["NOV"])  # const + NOV
    y = df_clean["DIC"]
    modelo = sm.OLS(y, X).fit()

    # Asignar predicciones y residuos al DataFrame original alineando por indice
    df.loc[df_clean.index, "Predicha"] = modelo.predict(X)
    df.loc[df_clean.index, "Residuo"] = modelo.resid

    # residuo estandarizado (global)
    if not df["Residuo"].dropna().empty:
        df["Residuo_std"] = df["Residuo"] / df["Residuo"].std()
    else:
        df["Residuo_std"] = np.nan

    return modelo, df

