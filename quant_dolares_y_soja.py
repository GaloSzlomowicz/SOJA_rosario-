# ======= METRICAS SIMPLES DE TRADING ==============
# --------------------------------------------------
def calcular_metricas_trading(data, cfg=CONFIG):
    df = data.copy()
    umbral = cfg.get("zscore_umbral", 1.5)

    señales_compra = int((df["Se\u00f1al"] == 1).sum())
    señales_venta = int((df["Se\u00f1al"] == -1).sum())

    # Probabilidad simple de reversi\u00f3n al próximo paso: si en t|z|>umbral y |z_{t+1}|<|z_t|
    zcols = ["Zscore_Residuo_roll", "Zscore_Residuo", "Zscore_Spread"]
    zcol = next((c for c in zcols if c in df.columns and not df[c].dropna().empty), None)

    prob_reversion = np.nan
    if zcol is not None:
        z = df[zcol].values
        reversiones = 0
        total_extremos = 0
        for i in range(len(z)-1):
            if np.isnan(z[i]):
                continue
            if abs(z[i]) > umbral:
                total_extremos += 1
                if not np.isnan(z[i+1]) and abs(z[i+1]) < abs(z[i]):
                    reversiones += 1
        prob_reversion = (reversiones / total_extremos * 100) if total_extremos > 0 else np.nan

    metricas = {
        "señales_compra": señales_compra,
        "señales_venta": señales_venta,
        "prob_reversion_pct": prob_reversion,
        "zcol_usada": zcol
    }
    return metricas

