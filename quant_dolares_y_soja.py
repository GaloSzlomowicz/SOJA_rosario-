# ======= PANEL GRÁFICO COMPACTO ====================
# --------------------------------------------------
def panel_visual(data, resultados_coint=None, metricas=None, cfg=CONFIG):
    df = data.copy()
    fig, axs = plt.subplots(2, 3, figsize=(16, 9))
    fig.suptitle("Sistema de Trading de Reversi\u00f3n — NOV vs DIC", fontsize=14)

    # 1 - Spread
    axs[0,0].plot(df['Fecha'], df['Spread'], lw=1)
    axs[0,0].axhline(df['Spread'].mean(), linestyle='--')
    axs[0,0].set_title('Spread (DIC - NOV)')

    # 2 - Zscore elegido
    zcol = metricas.get('zcol_usada') if metricas else None
    if zcol and zcol in df.columns:
        axs[0,1].plot(df['Fecha'], df[zcol], lw=1)
        axs[0,1].axhline(0, color='black', lw=0.8)
        axs[0,1].axhline(cfg['zscore_umbral'], color='green', ls='--')
        axs[0,1].axhline(-cfg['zscore_umbral'], color='red', ls='--')
        axs[0,1].set_title(f'Z-score (usando: {zcol})')
    else:
        axs[0,1].text(0.5,0.5,'No hay Z-score calculado', ha='center')

    # 3 - residuos y señales
    if 'Residuo' in df.columns:
        axs[0,2].plot(df['Fecha'], df['Residuo'], lw=1)
        buy = df[df['Se\u00f1al']==1]
        sell = df[df['Se\u00f1al']==-1]
        axs[0,2].scatter(buy['Fecha'], buy['Residuo'], marker='^', color='green')
        axs[0,2].scatter(sell['Fecha'], sell['Residuo'], marker='v', color='red')
        axs[0,2].axhline(0, ls='--')
        axs[0,2].set_title('Residuos y Señales')

    # 4 - hist residuo
    if 'Residuo' in df.columns:
        axs[1,0].hist(df['Residuo'].dropna(), bins=30, density=False)
        axs[1,0].set_title('Histograma de residuos')

    # 5 - QQ plot
    if 'Residuo' in df.columns:
        stats.probplot(df['Residuo'].dropna(), dist='norm', plot=axs[1,1])
        axs[1,1].set_title('Q-Q plot')

    # 6 - texto resumen
    axs[1,2].axis('off')
    resumen = []
    if resultados_coint:
        resumen.append(f"coint p-value: {resultados_coint.get('coint_pvalue', np.nan):.4f}")
        resumen.append(f"ADF spread p-value: {resultados_coint.get('adf_pvalue', np.nan):.4f}")
        resumen.append(f"ADF resid p-value: {resultados_coint.get('adf_resid_pvalue', np.nan):.4f}")
    if metricas:
        resumen.append(f"Señales compra: {metricas.get('señales_compra',0)}")
        resumen.append(f"Señales venta: {metricas.get('señales_venta',0)}")
        resumen.append(f"Prob rev (%): {metricas.get('prob_reversion_pct',np.nan):.2f}")
    axs[1,2].text(0,0.9, '\n'.join(resumen), fontsize=10)

    plt.tight_layout(rect=[0,0,1,0.96])
    plt.show()

