# --------------------------------------------------
CONFIG = {
    "excel_dic": "DICIEMBRE2.xlsx",   # archivo con precios de DIC
    "excel_nov": "NOVIEMBRE1.xlsx",  # archivo con precios de NOV
    "fecha_col": 0,                    # indice de la columna con fecha en Excel (0-based)
    "precio_col": 11,                  # indice de la columna con precio en Excel (0-based)
    "zscore_umbral": 1.5,              # umbral para señales
    "coint_pvalue_max": 0.05,          # p-value max para considerar cointegración
    "rolling_window": 60,              # ventana rolling para z-score adaptativo (en observaciones)
    "output_excel": "resultado_reversion.xlsx", # archivo donde exportar tablas
}

# --------------------------------------------------
# ======= CARGA Y EMPALME DE SERIES ================
# --------------------------------------------------
def cargar_series(cfg=CONFIG):
    """Carga los Excel y entrega un DataFrame con Fecha, NOV, DIC y Spread.
    Se asume que cada Excel no tiene header y las columnas se eligen por indices.
    """
    if not os.path.exists(cfg["excel_nov"]):
        raise FileNotFoundError(f"Archivo NOV no encontrado: {cfg['excel_nov']}")
    if not os.path.exists(cfg["excel_dic"]):
        raise FileNotFoundError(f"Archivo DIC no encontrado: {cfg['excel_dic']}")

    nov = pd.read_excel(cfg["excel_nov"], header=None)
    dic = pd.read_excel(cfg["excel_dic"], header=None)

    # Normalizar columnas Fecha y Precio
    nov_cols = nov.copy()
    dic_cols = dic.copy()

    nov_cols["Fecha"] = pd.to_datetime(nov_cols.iloc[:, cfg["fecha_col"]], errors="coerce")
    dic_cols["Fecha"] = pd.to_datetime(dic_cols.iloc[:, cfg["fecha_col"]], errors="coerce")

    nov_cols["Precio"] = pd.to_numeric(nov_cols.iloc[:, cfg["precio_col"]], errors="coerce")
    dic_cols["Precio"] = pd.to_numeric(dic_cols.iloc[:, cfg["precio_col"]], errors="coerce")

    # Filtrar fechas en común
    fechas_comunes = sorted(set(nov_cols["Fecha"]).intersection(set(dic_cols["Fecha"])))
    if len(fechas_comunes) == 0:
        raise ValueError("No hay fechas comunes entre los archivos. Verificá las columnas de fecha.")

    nov_f = nov_cols[nov_cols["Fecha"].isin(fechas_comunes)].sort_values("Fecha").reset_index(drop=True)
    dic_f = dic_cols[dic_cols["Fecha"].isin(fechas_comunes)].sort_values("Fecha").reset_index(drop=True)

    # Drop NaNs de Precio y volver a sincronizar por Fecha
    nov_f = nov_f.dropna(subset=["Precio"]).reset_index(drop=True)
    dic_f = dic_f.dropna(subset=["Precio"]).reset_index(drop=True)

    fechas_final = sorted(set(nov_f["Fecha"]).intersection(set(dic_f["Fecha"])))
    nov_final = nov_f[nov_f["Fecha"].isin(fechas_final)].sort_values("Fecha").reset_index(drop=True)
    dic_final = dic_f[dic_f["Fecha"].isin(fechas_final)].sort_values("Fecha").reset_index(drop=True)

    data = pd.DataFrame({
        "Fecha": nov_final["Fecha"],
        "NOV": nov_final["Precio"].values,
        "DIC": dic_final["Precio"].values,
    })
    data = data.dropna(subset=["NOV", "DIC"]).reset_index(drop=True)
    data["Spread"] = data["DIC"] - data["NOV"]

    print(f"Fechas comunes: {data['Fecha'].min().date()} -> {data['Fecha'].max().date()} | Observaciones: {len(data)}")
    return data

